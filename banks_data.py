def search_banks(query: str, limit: int = 10) -> list:
    """
    銀行名を検索する
    
    Args:
        query: 検索キーワード
        limit: 最大返却数
    
    Returns:
        マッチした銀行名のリスト
    """
    if not query:
        return []
    
    query = query.strip()
    results = []
    
    # 完全一致を優先
    for bank in ALL_BANKS:
        if query == bank:
            results.append(bank)
    
    # 前方一致
    for bank in ALL_BANKS:
        if bank.startswith(query) and bank not in results:
            results.append(bank)
    
    # 部分一致
    for bank in ALL_BANKS:
        if query in bank and bank not in results:
            results.append(bank)
    
    return results[:limit]
